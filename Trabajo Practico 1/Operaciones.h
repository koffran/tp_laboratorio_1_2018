/** \brief Pide un numero al usuario
 *
 * \param Mensaje de texto con pedido del numero correspondiente
 * \return Regresa un numero de tipo float
 *
 */
float getNum(char[]);

/** \brief Realiza la suma entre dos numeros
 *
 * \param Representa un numero de tipo flotante
 * \param Representa un numero de tipo flotante
 * \return Devuelve el resultado de la suma en tipo flotante
 *
 */
float suma (float , float);

/** \brief Realiza la resta entre dos numeros
 *
 * \param Representa un numero de tipo flotante
 * \param Representa un numero de tipo flotante
 * \return Devuelve el resultado de la resta en tipo flotante
 *
 */
float resta (float , float);

/** \brief Realiza la division entre dos numeros
 *
 * \param Representa un numero de tipo flotante
 * \param Representa un numero de tipo flotante
 * \return Devuelve el resultado de la division en tipo flotante
 *
 */
float division (float , float);

/** \brief Realiza la multiplicacion entre dos numeros
 *
 * \param Representa un numero de tipo flotante
 * \param Representa un numero de tipo flotante
 * \return Devuelve el resultado de la multiplicacion en tipo flotante
 *
 */
float multiplicacion (float , float);

/** \brief Realiza el factorial de un numero
 *
 * \param Representa un numero de tipo flotante
 * \return Valor de tipo long long int que representa el factorial de un numero menor o igual a 20.
 *
 */
long long int factorial (float);
